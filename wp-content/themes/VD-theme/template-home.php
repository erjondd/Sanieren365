<script>
    jQuery(".section-main-buton a").click(function() {
        jQuery('html, body').animate({
            scrollTop: jQuery(".home-pages").offset().top
        }, 1000);
    });
    jQuery(".reklam-button a").click(function() {
        jQuery('html, body').animate({
            scrollTop: jQuery(".home-pages").offset().top
        }, 1000);
    });

    //Module Add Scroll
    var allModules = jQuery('.module');
    jQuery(document).on('scroll', function() {
        var scroll = jQuery(document).scrollTop();
        allModules.each(function(i, elm) {
            if (jQuery(elm).is(":visible")) {
                jQuery(elm).addClass('module-ready');
            }
        })

        // console.log(allModules);
    });

    //scrolli n'counter
    var a = 0;
    jQuery(window).scroll(function() {

        var oTop = jQuery('#counter').offset().top - window.innerHeight;
        if (a == 0 && jQuery(window).scrollTop() > oTop) {
            jQuery('.counter-value').each(function() {
                var $this = jQuery(this),
                    countTo = $this.attr('data-count');
                jQuery({
                    countNum: $this.text()
                }).animate({
                    countNum: countTo
                }, {
                    duration: 2000,
                    easing: 'swing',
                    step: function() {
                        $this.text(Math.floor(this.countNum));
                    },
                    complete: function() {
                        $this.text(this.countNum);
                        //alert('finished');
                    }

                });
            });
            a = 1;
        }

    });
    jQuery('.count').each(function() {
        jQuery(this).prop('Counter', 0).animate({
            Counter: jQuery(this).text()
        }, {
            duration: 5000,
            easing: 'swing',
            step: function(now) {
                jQuery(this).text(Math.ceil(now));
            }
        });
    });
</script>